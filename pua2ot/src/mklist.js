/**
 * Figures out which members two arrays have in common and returns
 * an array with just those members.
 * 
 * @param {array} a - one of the arrays to compare.
 * @param {array} b - the other array to comnpare.
 * @returns {array} an array containing the common elements.
 */
function intersect(a, b) {
    var t;
    if (b.length > a.length) t = b, b = a, a = t;
    return a.filter(function (e) {
        return b.indexOf(e) > -1;
    });
}

/**
 * Copies text to the clipboard.
 * 
 * @param {string} t - The text to copy.
 */
async function toClipboard(t) {
    try {
        await navigator.clipboard.writeText(t);
    } catch (err) {
        console.error('Failed to copy text: ', err);
    }
}

/**
 * Sets up listeners that make many cells in the table
 * clickable.
 */
function setupListeners() {
    tbl_body.addEventListener('click', function(e) {
        tgt = e.target.closest('td');
        if (lastSelection != null) {
            lastSelection.style = '';
        }
        lastSelection = tgt;
        const cl = Array.from(tgt.classList);
        if (intersect(cl, otagpref).length > 0) {
            let tc = tgt.innerHTML;
            tc = tc.replaceAll("&amp;", "&");
            tc = tc.replaceAll("\u200D", "&zwj;");
            if (tc.length > 0) {
                tgt.style.backgroundColor = "DarkSeaGreen";
                dlg_header.innerHTML = "Character code:";
                info_para.innerText = tc;
                dialog.showModal();
           }
        } else if (cl.includes("basec")) {
            tgt.style.backgroundColor = "DarkSeaGreen";
            let hs = tgt.innerText;
            dlg_header.innerHTML = "Base code points:";
            info_para.innerText = makeHex(hs);
            dialog.showModal();
        }
    })
    copy_text.addEventListener('click', function(e) {
        toClipboard(info_para.innerText);
    });
}

