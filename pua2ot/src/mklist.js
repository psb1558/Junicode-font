/**
 * Converts a string hex code to a character.
 * 
 * @param {string} hexString - The hex code of the desired character.
 * @returns {string} A one-character string.
 */
function hexToChar(hexString) {
    let i = parseInt(hexString, 16);
    return String.fromCodePoint(i);
}

/**
 * For each character in a string, get the hex code and save it
 * in a space-separated list.
 * 
 * @param {string} s - A string to get the hex codes for.
 * @returns {string} string containing the hex codes for each character in a string.
 */
function makeHex(s) {
    let result = "";
    for (const c of s) {
        const i = c.codePointAt(0);
        let hexstr = i.toString(16).toUpperCase();
        while (hexstr.length < 4) {
            hexstr = '0' + hexstr;
        }
        if (result.length > 0) {
            result = result + ' ';
        }
        result = result + hexstr
    }
    return result;
}

